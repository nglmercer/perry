//! JSON handling — JSON.parse(), JSON.stringify(), and specialized variants
//!
//! Provides all core JSON functions used by compiled TypeScript programs.
//! These live in perry-runtime (not perry-stdlib) so that programs that
//! only use JSON don't need to link the full stdlib.
//!
//! The file was split (issue #1105 wave) into topical sub-modules:
//!
//! - `simd`         — SIMD string-terminator scanners
//! - `parser`       — `DirectParser` recursive-descent implementation
//! - `parse_api`    — `js_json_parse` / `js_json_parse_typed_array` FFI
//! - `stringify`    — core stringify traversal (object/array/scalar emitters)
//! - `stringify_api` — `js_json_stringify*` / `js_json_get_*` FFI entries
//! - `replacer`     — replacer + pretty/indent + array-replacer variants
//! - `reviver`      — `JSON.parse(text, reviver)` support
//!
//! Shared TLS state, file-local constants, and tiny helpers used by every
//! sibling stay in this module.

