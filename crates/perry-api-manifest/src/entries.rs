const fn method(
    module: &'static str,
    name: &'static str,
    has_receiver: bool,
    class_filter: Option<&'static str>,
) -> ApiEntry {
    method_entry(module, name, has_receiver, class_filter, true)
}

const fn internal_method(
    module: &'static str,
    name: &'static str,
    has_receiver: bool,
    class_filter: Option<&'static str>,
) -> ApiEntry {
    method_entry(module, name, has_receiver, class_filter, false)
}

const fn method_entry(
    module: &'static str,
    name: &'static str,
    has_receiver: bool,
    class_filter: Option<&'static str>,
    module_export: bool,
) -> ApiEntry {
    ApiEntry {
        module,
        name,
        kind: ApiKind::Method {
            has_receiver,
            class_filter,
        },
        source: ApiSource::Stdlib,
        stub: false,
        module_export: module_export && !has_receiver && class_filter.is_none(),
        abi_version: None,
        params: &[],
        returns: TypeSpec::Any,
    }
}

